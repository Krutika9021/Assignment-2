package asciival;

public class AsciiVal 
{
    public static void main(String[] args)
    {
        char ch = 'k';
        int ascii = ch;
        System.out.println("The ASCII value of character ' " + ch + " ' is : " + ascii);
        int castAscii = (int) ch;
        System.out.println("Ascii value is : " + castAscii);
    }
    
}

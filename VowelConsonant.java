package vowelconsonant;

public class VowelConsonant 
{
    public static void main(String[] args)
    {
        char ch = 'k';
        if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u')
        {
            System.out.println("Character is vowel. ");
        }
        else
        {
            System.out.println("Character is consonant");
        }
    }
}
